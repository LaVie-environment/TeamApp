    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public List<TaskDTO> getTasks() {
        return tasks;
    }

    public void setTasks(List<TaskDTO> tasks) {
        this.tasks = tasks;
    }

    private String name;
    private String roleName;
    private List<TaskDTO> tasks;

